// TOPIC: Abstract Data Type (ADT)

#include <iostream>
using namespace std;

/*
ADT (Abstract Data Type)
------------------------
ADT defines:

1. What data is stored
2. What operations can be performed

But it DOES NOT define how it is implemented.

Example: Stack ADT
Operations:

push()
pop()
peek()
isEmpty()

Implementation could be:

Array
Linked List

ADT only describes behaviour.
*/

int main()
{
    cout << "ADT defines behaviour not implementation" << endl;
}